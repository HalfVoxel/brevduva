use std::fmt::Display;

use serde::de::DeserializeOwned;

pub struct RawDeserializer<'de> {
    // This string starts with the input data and characters are truncated off
    // the beginning as data is parsed.
    input: &'de str,
}

impl<'de> RawDeserializer<'de> {
    // By convention, `Deserializer` constructors are named like `from_xyz`.
    // That way basic use cases are satisfied by something like
    // `serde_json::from_str(...)` while advanced use cases that require a
    // deserializer can make one with `serde_json::Deserializer::from_str(...)`.
    pub fn from_str(input: &'de str) -> Self {
        RawDeserializer { input }
    }
}

pub fn from_slice<T: DeserializeOwned>(input: &[u8]) -> Result<T, RawError> {
    let mut deserializer = RawDeserializer::from_str(std::str::from_utf8(input)?);
    T::deserialize(&mut deserializer)
}

#[derive(Debug, thiserror::Error)]
pub enum RawError {
    #[error("An error occurred")]
    Custom(String),
    #[error("Could not parse UTF-8 string")]
    Utf8Error(#[from] std::str::Utf8Error),
}

impl serde::de::Error for RawError {
    fn custom<T: Display>(msg: T) -> Self {
        Self::Custom(format!("{}", msg))
    }
}

impl serde::ser::Error for RawError {
    fn custom<T: Display>(msg: T) -> Self {
        Self::Custom(format!("{}", msg))
    }
}

