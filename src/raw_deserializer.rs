#[test]
fn test_raw_deserializer() {
    let mut deserializer = RawDeserializer::from_str("hello");
    let result: Result<String, RawError> =
        <String as serde::Deserialize>::deserialize(&mut deserializer);
    assert_eq!(result.unwrap(), "hello");
}
